//% Implements Fortran-callable wrappers around GSL functions.

#include <gsl/gsl_qrng.h>

const gsl_qrng_type *gsl_qrng_type_get(int i) {
  /* Return a GSL quasi-random number generator type */
  const gsl_qrng_type *qrng;
  switch(i) {
  case 1:
    qrng = gsl_qrng_niederreiter_2;
    break;
  case 2:
    qrng = gsl_qrng_sobol;
    break;
  case 3:
    qrng = gsl_qrng_halton;
    break;
  case 4:
    qrng =  gsl_qrng_reversehalton;
    break;
  default:
    qrng = NULL;
    break;
  }
  return qrng;
}
